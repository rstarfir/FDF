#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (i < len)
	{
		if (src[j] != '\0')
		{
			dst[i] = src[j];
			j++;
		}
		else
			dst[i] = '\0';
		i++;
	}
	return (dst);
}
